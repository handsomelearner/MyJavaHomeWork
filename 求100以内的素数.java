package page102works;

public class Tester03 {
static int Test(int a)
{
for(int i=2;i<a;i++)
{
if(a%i==0) return -1;
}
return a;
}
public static void main(String[] args) {
int k=0;
for(int i=2;i<=100;i++)
{
if(Test(i)!=-1)
{
System.out .printf("%d\t",Test(i));
k++;
if(k%5==0) System.out.println();
}
}

}

}
